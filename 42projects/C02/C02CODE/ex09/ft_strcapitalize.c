char	*ft_strcapitalize(char *str)
{
	int c;

	c = 0;
	ft_strlowcase(str);
	while (str[c] != '\0')
	{
		if (str[c] >= 'A' && str[c] <= 'Z')
			str[c] = str[c] - 32;
		c++;
	}
	return (str);
}
